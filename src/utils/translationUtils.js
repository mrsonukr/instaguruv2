import { getTranslation } from '../data/translations';

export const translateCategories = (categories, language = 'en') => {
  return categories.map(category => {
    const translatedCategory = {
      ...category,
      name: getTranslation(`${category.slug}.name`, language),
      description: getTranslation(`${category.slug}.description`, language),
      label: getTranslation(`${category.slug}.label`, language),
      placeholder: getTranslation(`${category.slug}.placeholder`, language),
      filters: {}
    };

    // Translate filters
    Object.keys(category.filters).forEach(filterKey => {
      const filterName = filterKey.toLowerCase();
      translatedCategory.filters[filterKey] = {
        label: getTranslation(`${category.slug}.filters.${filterName}`, language),
        placeholder: category.filters[filterKey].placeholder
      };
    });

    return translatedCategory;
  });
};

